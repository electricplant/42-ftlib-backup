#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	ssize_t	len;

	len = (ssize_t)ft_strlen((char *)s);
	while (len >= 0)
	{
		if (s[len] == (char)c)
			return ((char *)&s[len]);
		len--;
	}
	return (NULL);
}
